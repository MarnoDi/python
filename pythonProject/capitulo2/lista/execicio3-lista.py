raio = int(input("Digite o raio do círculo: "))
pi = 3.14159

diametro = 2 * raio
circunferencia = 2 * pi * raio
area = pi * (raio ** 2)

print("o diametro deu: ", diametro)
print("a circunferencia deu:  =", circunferencia)
print("a area deu:  =", area)

